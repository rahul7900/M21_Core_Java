package com.operator.intro;

public class Bitwise {

	public static void main(String[] args) {
		int a=6,b =2;
		int res = a&b;
		int res1 =a|b;
		int res2 =a^b;
		int res3 =a>>b;
		System.out.println(res);
		System.out.println(res1);
		System.out.println(res2);
		System.out.println(res3);
		
	}

}
